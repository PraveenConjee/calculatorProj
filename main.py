first_number = int(input("Enter start year: "))
second_number = int(input("Enter end year: "))

add = first_number+second_number
sub = first_number-second_number
mult = first_number*second_number
div = first_number/second_number

print(first_number, "+", second_number, "=", add)
print(first_number, "-", second_number, "=", sub)
print(first_number, "*", second_number, "=", mult)
print(first_number, "/", second_number, "=", div)
